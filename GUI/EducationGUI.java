    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            double monthlyIncome = Double.parseDouble(jTextField1.getText());
            String continent = jComboBox1.getSelectedItem().toString();

            double avg = 0;
            String fact = "";

            switch (continent) {
                case "Europe":
                    avg = 35000;
                    fact = "Europe focuses on fair work and social welfare.";
                    break;
                case "Asia":
                    avg = 15000;
                    fact = "Asia's growing economies boost job creation.";
                    break;
                case "North America":
                    avg = 42000;
                    fact = "North America has high employment rates and wages.";
                    break;
                case "South America":
                    avg = 9000;
                    fact = "South America is improving sustainable work equality.";
                    break;
                case "Africa":
                    avg = 5000;
                    fact = "Africa focuses on job accessibility and fair pay.";
                    break;
                case "Australia":
                    avg = 38000;
                    fact = "Australia promotes balanced work-life culture.";
                    break;
            }

            IncomeComparator comp = new IncomeComparator(continent, avg, fact, monthlyIncome);
            jTextArea1.setText(comp.compareIncome() + "\n" + comp.similarityComment(monthlyIncome * 12));

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for income.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

